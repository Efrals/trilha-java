package edu.efraim.cod3operadores;

public class Operadores3 {
    public static void main(String[] args) {
        int numero = 10;

        //negativa o número
        numero = - numero;
        System.out.println(numero);

        //restaura número em positivo
        numero = numero * -1;
        System.out.println(numero);
    }
}
