package edu.efraim.cod3operadores;

public class Operadores4UnariosIteracoes {
    public static void main(String[] args) {
        int numero = 10;

        //far√° numero = numero +1
        numero++;
        numero++;
        numero++;
        System.out.println(numero);

        System.out.println(numero ++);

        System.out.println(++ numero);

        System.out.println(-- numero);

        System.out.println(numero --);

        numero--;
        numero--;
        numero--;
        System.out.println(numero);
    }
}
